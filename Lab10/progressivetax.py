def progressivetax(income):
    '''solution'''
    tax = 0
    if income <= 150000:
        tax = 0
    elif income <= 300000:
        tax = (income - 150000) * 0.05
    elif income <= 500000:
        tax = (150000 * 0.05) + ((income - 300000) * 0.10)
    elif income <= 750000:
        tax = (150000 * 0.05) + (200000 * 0.10) +  \
            ((income - 500000) * 0.15)
    elif income <= 1000000:
        tax = (150000 * 0.05) + (200000 * 0.10) +  \
            (250000 * 0.15) + ((income - 750000) * 0.20)
    elif income <= 2000000:
        tax = (150000 * 0.05) + (200000 * 0.10) +  \
            (250000 * 0.15) + (250000 * 0.20) + ((income - 1000000) * 0.25)
    elif income <= 4000000:
        tax = (150000 * 0.05) + (200000 * 0.10) + \
              (250000 * 0.15) + (250000 * 0.20) +  \
                (1000000 * 0.25) + ((income - 2000000) * 0.30)
    else:
        tax = (150000 * 0.05) + (200000 * 0.10) +  \
            (250000 * 0.15) + (250000 * 0.20) +  \
                (1000000 * 0.25) + (2000000 * 0.30) + ((income - 4000000) * 0.35)
    print(int(tax))

progressivetax(int(input()))
